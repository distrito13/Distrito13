package fatec.edu.models;


public class QuestionarioPtVI {
		private int id;
		private double valor;
		private String tipoAtivo;
		private int idTomador;
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public double getValor() {
			return valor;
		}
		public void setValor(double valor) {
			this.valor = valor;
		}
		public String getTipoAtivo() {
			return tipoAtivo;
		}
		public void setTipoAtivo(String tipoAtivo) {
			this.tipoAtivo = tipoAtivo;
		}
		public int getIdTomador() {
			return idTomador;
		}
		public void setIdTomador(int idTomador) {
			this.idTomador = idTomador;
		}
		
		
				
}
